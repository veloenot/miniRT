#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*u_b;

	i = 0;
	u_b = (unsigned char *)s;
	while (i < n)
	{
		u_b[i] = 0;
		i++;
	}
}
